#!/usr/bin/env node
const fs = require('fs');
const path = require('path');
const crypto = require('crypto');
const { renderShell, renderRelatedCarousel, renderSidebar } = require('../fe/src/ssg/render-shell');

const ROOT = process.cwd();
const CONFIG_PATH = path.join(ROOT, 'fe/src/ssg/static-pages.json');
const DATA_DIR = path.join(ROOT, 'fe/src/data');
const SSG_DIR = path.join(ROOT, 'fe/src/ssg');
const CACHE_DIR = path.join(ROOT, 'fe/.cache');
const CACHE_PATH = path.join(CACHE_DIR, 'static-pages-cache.json');
const MANIFEST_SRC = path.join(DATA_DIR, 'services.manifest.json');
const SCHEMA_SRC = path.join(DATA_DIR, 'services.schema.json');
const RELATED_SRC = path.join(DATA_DIR, 'related-services.json');
const SITE_SETTINGS_SRC = path.join(DATA_DIR, 'site-settings.json');
const MANIFEST_OUT = path.join(ROOT, 'fe/public/dunsmile/services.manifest.json');
const SCHEMA_OUT = path.join(ROOT, 'fe/public/dunsmile/services.schema.json');
const SITE_SETTINGS_OUT = path.join(ROOT, 'fe/public/dunsmile/site-settings.json');
const GENERATED_BANNER = '<!-- generated by scripts/build-static-pages.js; do not edit directly -->\n';

function readUtf8(filePath) {
  return fs.readFileSync(path.join(ROOT, filePath), 'utf8');
}

function writeUtf8(filePath, content) {
  const target = path.join(ROOT, filePath);
  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.writeFileSync(target, content);
}

function copyFile(source, target) {
  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.copyFileSync(source, target);
}

function parseArgs(argv) {
  const args = argv.slice(2);
  const incrementalByArg = args.includes('--incremental');
  const incrementalByEnv = process.env.BUILD_INCREMENTAL === '1';
  return {
    incremental: incrementalByArg || incrementalByEnv,
  };
}

function sha1(value) {
  return crypto.createHash('sha1').update(String(value || ''), 'utf8').digest('hex');
}

function readCache() {
  if (!fs.existsSync(CACHE_PATH)) return { pages: {}, fileDigests: {} };
  try {
    const parsed = JSON.parse(fs.readFileSync(CACHE_PATH, 'utf8'));
    if (!parsed || typeof parsed !== 'object') return { pages: {}, fileDigests: {} };
    if (!parsed.pages || typeof parsed.pages !== 'object') parsed.pages = {};
    if (!parsed.fileDigests || typeof parsed.fileDigests !== 'object') parsed.fileDigests = {};
    return parsed;
  } catch (_error) {
    return { pages: {}, fileDigests: {} };
  }
}

function writeCache(cache) {
  fs.mkdirSync(CACHE_DIR, { recursive: true });
  fs.writeFileSync(CACHE_PATH, `${JSON.stringify(cache, null, 2)}\n`);
}

function getFileDigest(absPath, cache) {
  if (!fs.existsSync(absPath)) return sha1('');
  const stat = fs.statSync(absPath);
  const signature = `${stat.mtimeMs}:${stat.size}`;
  const prev = cache.fileDigests[absPath];
  if (prev && prev.signature === signature && prev.hash) return prev.hash;
  const hash = sha1(fs.readFileSync(absPath, 'utf8'));
  cache.fileDigests[absPath] = { signature, hash };
  return hash;
}

function collectGlobalHash(cache) {
  const globalInputs = [
    getFileDigest(CONFIG_PATH, cache),
    getFileDigest(path.join(SSG_DIR, 'render-shell.js'), cache),
    getFileDigest(path.join(SSG_DIR, 'partials', 'header.html'), cache),
    getFileDigest(path.join(SSG_DIR, 'partials', 'sidebar.html'), cache),
    getFileDigest(path.join(SSG_DIR, 'partials', 'settings-modal.html'), cache),
    getFileDigest(path.join(SSG_DIR, 'partials', 'related-card.html'), cache),
    getFileDigest(path.join(SSG_DIR, 'partials', 'related-carousel.html'), cache),
    getFileDigest(path.join(SSG_DIR, 'partials', 'carousel-arrow-icon.html'), cache),
    getFileDigest(MANIFEST_SRC, cache),
    getFileDigest(SCHEMA_SRC, cache),
    getFileDigest(RELATED_SRC, cache),
    getFileDigest(SITE_SETTINGS_SRC, cache),
    getFileDigest(path.join(DATA_DIR, 'shell-ui.json'), cache),
  ].join('\n---\n');
  return sha1(globalInputs);
}

function collectPageHash(page, globalHash, cache) {
  const pageInputs = [
    globalHash,
    JSON.stringify(page),
    getFileDigest(path.join(ROOT, page.template), cache),
  ];

  if (page.shell) {
    pageInputs.push(getFileDigest(path.join(ROOT, page.shell.content), cache));
    pageInputs.push(getFileDigest(path.join(ROOT, page.shell.actions), cache));
  } else if (page.body) {
    pageInputs.push(getFileDigest(path.join(ROOT, page.body), cache));
  }

  return sha1(pageInputs.join('\n---\n'));
}

function assertPlaceholder(template, pageId) {
  if (!template.includes('{{BODY}}')) {
    throw new Error(`[${pageId}] template must include {{BODY}} placeholder`);
  }
}

function renderAdSlot(position, page) {
  return '';
}

function renderPageBody(page) {
  const serviceId = (page.shell && page.shell.activeServiceId) || page.id || '';
  const applyBodyTemplates = (raw) => String(raw || '').replace(/\{\{RELATED_CAROUSEL\}\}/g, renderRelatedCarousel(serviceId));

  if (page.shell) {
    const content = applyBodyTemplates(readUtf8(page.shell.content).trim());
    const rawActions = readUtf8(page.shell.actions).trim();
    const hasActionsCarousel = rawActions.includes('{{RELATED_CAROUSEL}}');
    const actions = rawActions.replace(/\{\{RELATED_CAROUSEL\}\}/g, '').trim();
    const postMainContent = hasActionsCarousel ? renderRelatedCarousel(serviceId) : '';
    return renderShell({
      activeServiceId: page.shell.activeServiceId,
      themeClass: page.shell.themeClass,
      pageTitle: page.shell.pageTitle,
      heroEyebrow: page.shell.heroEyebrow,
      heroTitle: page.shell.heroTitle,
      heroSummary: page.shell.heroSummary,
      heroPresetClass: page.shell.heroPresetClass,
      mainContent: content,
      adSlotTop: renderAdSlot('top', page),
      adSlotBottom: renderAdSlot('bottom', page),
      actions,
      postMainContent,
    }).trim();
  }

  if (!page.body) {
    throw new Error(`[${page.id}] either 'body' or 'shell' must be provided`);
  }

  return applyBodyTemplates(readUtf8(page.body).trim());
}

function injectLegacySidebar(body, page) {
  const activeServiceId = (page.shell && page.shell.activeServiceId) || page.id || '';
  const sidebarHtml = renderSidebar(activeServiceId).trim();
  const sidebarPattern = /<div id="serviceMenuBackdrop" class="dp-sidebar-overlay"[\s\S]*?<\/aside>/;
  if (!sidebarPattern.test(body)) return body;
  return body.replace(sidebarPattern, sidebarHtml);
}

function build() {
  const opts = parseArgs(process.argv);
  if (!fs.existsSync(CONFIG_PATH)) {
    throw new Error(`Config missing: ${CONFIG_PATH}`);
  }

  const config = JSON.parse(fs.readFileSync(CONFIG_PATH, 'utf8'));
  const pages = Array.isArray(config.pages) ? config.pages : [];
  if (pages.length === 0) {
    throw new Error('No pages configured in fe/src/ssg/static-pages.json');
  }

  const cache = opts.incremental ? readCache() : { pages: {}, fileDigests: {} };
  const globalHash = collectGlobalHash(cache);
  const results = [];
  if (!fs.existsSync(MANIFEST_SRC)) {
    throw new Error(`Missing source manifest: ${MANIFEST_SRC}`);
  }
  if (!fs.existsSync(SCHEMA_SRC)) {
    throw new Error(`Missing source schema: ${SCHEMA_SRC}`);
  }
  if (!fs.existsSync(SITE_SETTINGS_SRC)) {
    throw new Error(`Missing source site settings: ${SITE_SETTINGS_SRC}`);
  }
  copyFile(MANIFEST_SRC, MANIFEST_OUT);
  copyFile(SCHEMA_SRC, SCHEMA_OUT);
  copyFile(SITE_SETTINGS_SRC, SITE_SETTINGS_OUT);
  results.push(`services-manifest: fe/public/dunsmile/services.manifest.json`);
  results.push(`services-schema: fe/public/dunsmile/services.schema.json`);
  results.push(`site-settings: fe/public/dunsmile/site-settings.json`);

  for (const page of pages) {
    const pageHash = collectPageHash(page, globalHash, cache);
    const outputAbsPath = path.join(ROOT, page.output);
    const canSkip = opts.incremental
      && cache.pages[page.id]
      && cache.pages[page.id] === pageHash
      && fs.existsSync(outputAbsPath);

    if (canSkip) {
      results.push(`${page.id}: ${page.output} (skipped)`);
      continue;
    }

    const template = readUtf8(page.template);
    let body = renderPageBody(page);
    body = injectLegacySidebar(body, page);

    assertPlaceholder(template, page.id);

    let rendered = template.replace('{{BODY}}', body);
    if (body.includes('data-related-carousel-service=') && !rendered.includes('/dunsmile/js/carousel-controls.js')) {
      rendered = rendered.replace('</body>', '  <script src="/dunsmile/js/carousel-controls.js" defer></script>\n</body>');
    }

    const html = GENERATED_BANNER + rendered + '\n';
    writeUtf8(page.output, html);
    if (opts.incremental) cache.pages[page.id] = pageHash;
    results.push(`${page.id}: ${page.output}`);
  }

  if (opts.incremental) writeCache(cache);
  return results;
}

try {
  const built = build();
  console.log('Static pages built:');
  for (const line of built) console.log(`- ${line}`);
} catch (error) {
  console.error(error.message || String(error));
  process.exit(1);
}
